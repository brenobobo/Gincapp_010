package br.com.naosai.breno.gincapp_010.Entidades;

public class Esporte {
    String id;
    String descricao;

    public Esporte() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
}
